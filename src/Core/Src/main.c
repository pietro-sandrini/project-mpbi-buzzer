  TIM1->ARR = ARR;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  switch (stato) {
		case 0:
			disattivaPWM(&htim1);
			stato = 1;
			break;
		case 1:
			//LETTURA PULSANTE PER INIZIARE UNA CANZONE
			btVal = !HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);
			if(btVal == 1)
			{
				stato = 2;
				break;
			}

			//LETTURA SERIALE PER EVENTUALE DATI IN INGRESSO
			HAL_UART_Receive(&huart2, rx_buf, 34, 200);// UART RX
			for (i = 0; i < (bufSize-2); i++)
			{
				if((rx_buf[i+1] == '\r') && rx_buf[i+2] == '\n')
				{
					fLetta = atoi(rx_buf);
					memset(rx_buf,0, bufSize); //CLEAR DEL BUFFER DI RICEZIONE DOPO LA LETTURA
					stato = 6;
					break;
				}
			}
			break;
		case 2:
			attivaPWM(&htim1, DC_DEFAULT);
			stato = 3;
			break;
		case 3:
			//SELEZIONE DELLA NOTA DA SUONARE
			if(idNota > noteSize)
			{
				idNota = 0;
				stato = 0;
				break;
			}

			nota = melody[idNota]; //SELEZIONO LA NOTA DALLA MELODIA
			divider = melody[idNota+1]; //SELEZIONO IL CORRISPONDENTE DIVIDER

			psc = calcolaPrescaler(nota); // CALCOLO IL PRESCLAER DA ASSEGNARE AL TIMER
			noteDuration = calcolaNoteDuration(divider, WHOLE_NOTE); //CALCOLO LA DURATA DELLA NOTA
			if(psc == -1) //SE IL PSC = -1 SIGNIFICA CHE NON DEVO RIPRODURRE NULLA
			{
				psc = 1; //SETTO PSC A 1 IN MODO DA POTER CALCOLARE COMUNQUE IL TEMPO DELLA PAUSA
				setDutyCycle(0); //SETTO IL DUTY CYCLE A 0
			}
			else
			{
				setDutyCycle(DC_DEFAULT); //SETTO IL DUTY CYCLE A QUELLO DI DEFAULT
			}

			//CALCOLA IL CONTEGGIO A CUI DEVE ARRIVARE PER OTTENERE LA DURATA DESIDERATA
			//PER OTTENERE UNA PAUSA AGGIUNTIVA TRA UNA NOTA E L'ALTRA FACCIO IL PLAY DELLA NOTA
			//PER 90% DEL TEMPO DI NOTEDURATION, QUESTO E FATTO PER AVERE MAGGIORE MUSICALITA
			maxCount = (int) calcolaConteggio(nota, psc, noteDuration*0.9);
			TIM1->PSC = psc; //SET DEL PRESCALER DEL TIMER
			stato = 4;
			break;
		case 4:
			//PLAY DELLA NOTA
			if(((TIM1->SR) & 0x01) == 0x01)
			{
				countCicli++;
				if(countCicli == maxCount)
				{
					countCicli = 0;
					TIM1->SR = TIM1->SR & ~0x01; //RESET TIMER DOPO IL CONTEGGIO MASSIMO
					setDutyCycle(0); //SET DUTY CYCLE A ZERO PER LA PAUSA
					stato = 5;
					break;
				}
				TIM1->SR = TIM1->SR & ~0x01; //RESET TIMER DOPO IL CONTEGGIO MASSIMO
			}
			break;
		case 5:
			if(((TIM1->SR) & 0x01) == 0x01)
			{
				countCicli++;
				if(countCicli == maxCount)
				{
					countCicli = 0;
					TIM1->SR = TIM1->SR & ~0x01; //RESET TIMER DOPO IL CONTEGGIO MASSIMO
					idNota += 2; //INCREMENTO DI DUE L'idNota, IN MODO DA OTTENERE LA SEGUENTE
					stato = 3;
					break;
				}
				TIM1->SR = TIM1->SR & ~0x01; //RESET TIMER DOPO IL CONTEGGIO MASSIMO
			}
			break;
		case 6:
			attivaPWM(&htim1, DC_DEFAULT);
			stato = 7;
			break;
		case 7:
			//PLAY DI UNA SINGOLA NOTA PER UN TEMPO STABILITO DI 3 SECONDI
			nota = fLetta;
			noteDuration = 3; //SETTO LA DURATA DELLA NOTA A 3 SECONDI
			psc = calcolaPrescaler(nota); //CALCOLO IL PRESCALER DA ASSEGNARE AL TIMER
			if(psc == -1)
			{
				psc = 1; //SETTO PSC A 1 IN MODO DA POTER CALCOLARE COMUNQUE IL TEMPO DELLA PAUSA
				setDutyCycle(0); //SETTO IL DUTY CYCLE A ZERO
			}
			else
			{
				setDutyCycle(DC_DEFAULT); //SETTO IL DUTY CYCLE A QUELLO DI DEFAULT
			}
			maxCount = (int) calcolaConteggio(nota, psc, noteDuration);
			TIM1->PSC = psc; //SETTO IL PRESCALER DEL TIMER
			countCicli = 0;
			stato = 8;
			break;
		case 8:
			if(((TIM1->SR) & 0x01) == 0x01)
			{
				countCicli++;
				if(countCicli == maxCount)
				{
					countCicli = 0;
					TIM1->SR = TIM1->SR & ~0x01; //RESET TIMER DOPO IL CONTEGGIO MASSIMO
					setDutyCycle(0); //SETTO DUTY CYCLE A ZERO
					stato = 0;
					break;
				}
				TIM1->SR = TIM1->SR & ~0x01; //RESET TIMER DOPO IL CONTEGGIO MASSIMO
			}
		default:
			break;
	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
