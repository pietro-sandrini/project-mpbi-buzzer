  //VARIABILI UART
  int bufSize = 7; //BUFSIZE 10000\r\n => 7 caratteri massimo (max frequency 10kHz)
  uint8_t rx_buf[bufSize]; //BUFFER DI RICEZIONE
  int i = 0; //INDICE DEL CICLO FOR
